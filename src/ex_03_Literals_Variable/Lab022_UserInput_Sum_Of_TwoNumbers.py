"""
Program to demonstrate the Sum,Mul,Div,Sub of Two numbers

"""

# Step 1: Building the logic for a program

# I/p: num1, num2
# o/p: sum, mul, div, sub (Always ask the interviewer what of datatype output you want) -> float

num1 = float(input("Enter the num1 value:"))
num2 = float(input("Enter the num2 value:"))

print(num1+num2)
print(num1*num2)
print(num1/num2)
print(num1-num2)


"""
Below program will demonstrate the use of int datatype in user input function

"""

num1 = int(input("Enter the num1 value:"))
num2 = int(input("Enter the num2 value:"))

print(num1+num2)
print(num1*num2)
print(num1/num2)
print(num1-num2)
