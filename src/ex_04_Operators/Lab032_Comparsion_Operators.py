"""
Program to demonstrate the Comparison operators

"""
a = float(input("Enter the value of a: "))
b = float(input("Enter the value of b: "))

print(a == b)  # o/p: False
print(a != b)  # o/p: True
print(a > b)  # o/p: True
print(a < b)  # o/p: False
print(a >= b)  # o/p: True
print(a <= b)  # o/p: False
