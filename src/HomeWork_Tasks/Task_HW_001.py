"""
Program to demonstrate the area of the triangle

"""
# Area of Triangle: 1/2 * base * height

# Step 1: Logic Building
# i/p: base = 10, height = 5
# o/p: Can be in float or any data type.


# Step 2: Rough Logic:
# Using input function with float data type for base and height
# Then adding the area of triangle formula and printing the value


base = float(input("Enter the value of base: "))
height = float(input("Enter the value of height: "))

area_of_triangle = (0.5 * (base * height))
print("Area of triangle is: ", area_of_triangle)
